public class NoAnimalFoundException extends Exception {

	public NoAnimalFoundException() {
		super();
	}

	public NoAnimalFoundException(String message, Throwable cause) {
		super(message, cause);
	}

	public NoAnimalFoundException(String message) {
		super(message);
	}

	public NoAnimalFoundException(Throwable cause) {
		super(cause);
	}

	/**
	 * 
	 */
	private static final long serialVersionUID = -4427284889613301995L;

}
